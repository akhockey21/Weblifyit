<?php namespace Hazzard\User;

use Hazzard\Support\ServiceProvider;

class BusinessInfoServiceProvider extends ServiceProvider {

	/**
	 * Indicates if loading of the provider is deferred.
	 *
	 * @var bool
	 */
	protected $defer = true;

	/**
	 * Register the service provider.
	 *
	 * @return void
	 */
	public function register()
	{
		$this->app->bindShared('weblifyit.BusinessInfo', function($app) {
			return new BusinessInfo($app['db']);
		});
	}

	/**
	 * Get the services provided by the provider.
	 *
	 * @return array
	 */
	public function provides()
	{
		return array('weblifyit.BusinessInfo');
	}
}